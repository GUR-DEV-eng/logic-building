

# Exercise 22
# Print 1 to 5 using while
i = 1
while i <= 5:
    print(i)
    i += 1
# Output: 1 2 3 4 5

# Exercise 23
# Countdown from 10 to 1
i = 10
while i >= 1:
    print(i)
    i -= 1
# Output: 10 9 8 ... 1

# Exercise 24
# Sum of 1 to 100
total = 0
i = 1
while i <= 100:
    total += i
    i += 1
print(total)
# Output: 5050